class UsersList {
  constructor(usersArray) {
    this.usersArray = usersArray;
  }


  showNames() {
    // let arrOfFirstName = arr.map((el) => el.first_name);
    // console.log(arrOfFirstName);    
    for (let i = 0; i < this.usersArray.length; i++) {
      console.log(this.usersArray[i].first_name);
    }
  }
  showById(id) {
    let filteredArray = this.usersArray.filter(function (obj) {
      if (obj.id == id) {
        return obj;
      }
    });
    if (filteredArray.length > 0) {
      console.log(filteredArray[0]);
    } else {
      console.log(`Unable to find user with id ${id}`)
      return '';
    }
  }

  addNewUser(newUser) {
    if (!newUser.first_name) {
      console.log('cannot add new user because first_name field is require');
      return;
    }
    let newId = this.getMaxId() + 1;
    newUser.id = newId;
    this.usersArray.push(newUser);
    console.log(`Hi everyone, i am + ${newUser.first_name}`)
  }

  getMaxId() {
    let maxId = -1;
    for (let i = 0; i < this.usersArray.length; i++) {
      if (this.usersArray[i].id > maxId) {
        maxId = this.usersArray[i].id;
      }
    }
    this.maxId = maxId;
    return maxId;
  }


  removeUserById(id) {
    let filteredArray = this.usersArray.filter(function (obj) {
      if (obj.id == id) {
        return obj;
      }
    });
    if (filteredArray.length > 0) {
      let index = this.usersArray.indexOf(filteredArray[0]);
      this.usersArray.splice(index, 1);
      console.log(`bye bye user with id ${id}`);
    } else {
      console.log(`Unable to find user with id ${id}`)
      return ''
    }
  }

  logUsersCount() {
    console.log(`total user count = ${this.usersArray.length} `);
  }

}

